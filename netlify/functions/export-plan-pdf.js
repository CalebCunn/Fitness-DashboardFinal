exports.handler = async (event) => {
  if (event.httpMethod !== "POST") return { statusCode: 405, body: "Method not allowed" };

  try {
    const { plan } = JSON.parse(event.body);
    if (!plan) return { statusCode: 400, body: "No plan provided" };

    const typeColors = {
      Rest: "#9ca3af", Easy: "#10b981", Interval: "#ef4444",
      Tempo: "#f59e0b", "Long Run": "#3b82f6", Gym: "#f97316",
    };

    const rows = plan.sessions.map(s => `
      <tr>
        <td style="padding:10px 14px;border-bottom:1px solid #f0f0f2;font-weight:600;color:#1d1d1f">${s.day}</td>
        <td style="padding:10px 14px;border-bottom:1px solid #f0f0f2"><span style="background:${typeColors[s.type]||"#f97316"}22;color:${typeColors[s.type]||"#f97316"};padding:3px 8px;border-radius:6px;font-size:12px;font-weight:600">${s.type}</span></td>
        <td style="padding:10px 14px;border-bottom:1px solid #f0f0f2;font-family:monospace;font-weight:700;color:#f97316">${s.dist||""}</td>
        <td style="padding:10px 14px;border-bottom:1px solid #f0f0f2;font-family:monospace;color:#f97316">${s.pace!=="N/A"?s.pace:""}</td>
        <td style="padding:10px 14px;border-bottom:1px solid #f0f0f2;color:#6e6e73;font-size:12px">${s.shoe!=="N/A"?s.shoe:""}</td>
        <td style="padding:10px 14px;border-bottom:1px solid #f0f0f2;color:#6e6e73;font-size:12px">${s.notes||""}</td>
      </tr>
    `).join("");

    const html = `<!DOCTYPE html>
<html>
<head>
<meta charset="utf-8">
<title>${plan.title}</title>
<style>
  * { margin:0; padding:0; box-sizing:border-box; }
  body { font-family: -apple-system, BlinkMacSystemFont, 'Helvetica Neue', sans-serif; background:#fff; color:#1d1d1f; padding:40px; }
  .header { margin-bottom:32px; padding-bottom:20px; border-bottom:2px solid #f0f0f2; }
  .badge { display:inline-block; background:#fff7ed; color:#f97316; font-size:11px; font-weight:700; letter-spacing:0.08em; text-transform:uppercase; padding:4px 10px; border-radius:20px; margin-bottom:12px; }
  h1 { font-size:28px; font-weight:800; letter-spacing:-0.03em; margin-bottom:6px; }
  .sub { font-size:14px; color:#6e6e73; }
  table { width:100%; border-collapse:collapse; margin-top:20px; }
  th { text-align:left; padding:10px 14px; background:#f5f5f7; font-size:11px; font-weight:700; letter-spacing:0.06em; text-transform:uppercase; color:#6e6e73; }
  tr:last-child td { border-bottom:none; }
  .footer { margin-top:32px; font-size:12px; color:#aeaeb2; text-align:center; padding-top:16px; border-top:1px solid #f0f0f2; }
</style>
</head>
<body>
  <div class="header">
    <div class="badge">Fitness Dashboard</div>
    <h1>${plan.title}</h1>
    <div class="sub">Caleb Cunningham · ${plan.startDate ? "Starting " + new Date(plan.startDate).toLocaleDateString("en-GB", {day:"numeric",month:"long",year:"numeric"}) : ""} · Running for Noah</div>
  </div>
  <table>
    <thead>
      <tr>
        <th>Day</th><th>Type</th><th>Distance</th><th>Pace</th><th>Shoe</th><th>Notes</th>
      </tr>
    </thead>
    <tbody>${rows}</tbody>
  </table>
  <div class="footer">Generated by Fitness Dashboard · Berlin Marathon 28 Sep 2026 · Get Kids Going</div>
</body>
</html>`;

    return {
      statusCode: 200,
      headers: { "Content-Type": "application/json", "Access-Control-Allow-Origin": "*" },
      body: JSON.stringify({ html }),
    };
  } catch (err) {
    return { statusCode: 500, body: JSON.stringify({ error: err.message }) };
  }
};
